public class ElementRev{
	public static void main(String[] args){
		int n=5;
		for(int i=0;i<n;i++){
		 int s=n-i;
			System.out.println("before swap the value is: " + i +" ====>||");
			System.out.println("                                 ||");
			System.out.println("after swap the value is: " + s  +"  <====||" );
			System.out.println();
		}
	}
}
		
